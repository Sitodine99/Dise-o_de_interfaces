    public void cambioTamano(String textoBtn){
        String[] textoBoton = textoBtn.split("x");
        String anchoStr = textoBoton[0];
        String altoStr = textoBoton[1];
        int ancho = Integer.parseInt(anchoStr);
        int alto = Integer.parseInt(altoStr);
        setSize(ancho,alto);
    }

